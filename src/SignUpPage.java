    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!jCheckBox1.isSelected()) {
        JOptionPane.showMessageDialog(null, "Please confirm you are not a robot.");
        return; // Exit the method if the checkbox is not checked
    }
    
       try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        con = DriverManager.getConnection("jdbc:mysql://localhost:3306/sep","root","system");
        char p1[] = pass1.getPassword();
        char p2[] = pass2.getPassword();
        String pass1 = new String(p1);
        String pass2 = new String(p2);

        if (!isValidPassword(pass1)) {
            JOptionPane.showMessageDialog(null, "Password must be at least 8 characters long and contain letters and numbers.");
            return;
        }

        if (pass1.equals(pass2)) {
            String sql = "INSERT INTO accounts(name,password,email) VALUES(?,?,?)";
            pst = con.prepareStatement(sql);
            pst.setString(1, name.getText());
            pst.setString(2, pass1);
            pst.setString(3, email.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Signup successful");
            new LogInPage().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Passwords do not match");
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "An error occurred: " + ex.getMessage());
    } finally {
        try {
            
            if (pst != null) pst.close();
            if (con != null) con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
}

private boolean isValidPassword(String password) {
    return password.length() >= 8 && password.matches(".*[A-Za-z].*") && password.matches(".*\\d.*");

    }//GEN-LAST:event_jButton1ActionPerformed

