// Method to toggle password visibility
private void togglePasswordVisibility(ImageIcon openEyeIcon, ImageIcon closedEyeIcon) {
    isPasswordVisible = !isPasswordVisible;

    if (isPasswordVisible) {
        pass1.setEchoChar((char) 0); // Show password
        jLabel7.setIcon(openEyeIcon); // Change to open eye icon
    } else {
        pass1.setEchoChar('*'); // Hide password
        jLabel7.setIcon(closedEyeIcon); // Change to closed eye icon
    }
}

